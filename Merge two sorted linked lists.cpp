    SinglyLinkedListNode* iter1 = head1, *iter2 = NULL;
    while (iter1->next) {
        iter1 = iter1->next;
    }

    iter1->next = head2;
    iter1 = head1;
    for (; iter1->next; iter1 = iter1->next) {
        for (iter2 = iter1->next; iter2; iter2 = iter2->next) {
            if (iter1->data > iter2->data) {
                int temp = iter1->data;
                iter1->data = iter2->data;
                iter2->data = temp;
            }
        }
    }

    return head1;

